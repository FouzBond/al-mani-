            <div align="justify">
  With the proclamation of the Mauritius Institute of Training and Development Act, the Institut Superieur de Technologie (IST) and the Swami Dayanand Institute of Management (SDIM) has come under the aegis of UTM.&nbsp; This new structural development represents a significant move for the long term expansion of the University.</div>

            <p>&nbsp;</p>

            <div align="justify">
  The University of Technology, Mauritius, as at 2010, and now in its tenth year of operation, is poised for a phase of significant expansion in line with the provisions of its Act and the national agenda of increasing access to tertiary education, capacity building in the context of the ongoing economic reforms, promotion of knowledge, research and innovation, as propounded by the Ministry of Tertiary Education, Science, Research and Technology. The University of Technology, Mauritius has the determination to become one of the leading tertiary education institutions of the region. UTM is a member of the Association of Commonwealth Universities. The forthcoming construction of the new campus at Cote DOr will enable the UTM to increase its number of students, the number of programmes offered and provide better facilities.</div><br/>

            <p>
              <strong>VISION</strong>&nbsp;&nbsp;
            </p>

            <p>
  To become a University of national, regional and international renown, providing multi-level quality tertiary education and training including continuing professional education geared towards sustained capacity building for increasingly technology-driven and enterprise-based developments.</p>

            <p>
              <strong>MISSION</strong>&nbsp;&nbsp; &nbsp;
            </p>

            <div align="justify">
  To offer a range of University programmes and activities in full-time, part-time and mixed modes to meet the changing needs of Mauritius and develop a regional and international dimension to its activities.&nbsp; UTM will aim for excellence along traditional as well as beyond traditional approaches to teaching, training, research and consultancy.</div><br/>
          </div>

          <p>
            <strong>AIM</strong>
          </p>


          <div align="justify">
  To provide access to a wide range of students for the acquisition of knowledge and skills with which they can make future professional contributions in various key sectors of the socio-economic and technological advancement of Mauritius and aspire to become dynamic players in regional and international activities.</div><br/>

