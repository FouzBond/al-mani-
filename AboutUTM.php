            <p align="justify" class="bodyst" style="text-align: justify;">
  &nbsp;<span class="contentheading">T</span>he University of Technology, Mauritius (UTM) was created in year 2000 by Act of Parliament as the Government implemented its plan to have a second University which would focus towards development of oriented sectors in particular. Two state institutions namely the Mauritius Institute of Public Administration and Management (MIPAM) and the State Information Training Centre (SITRAC Ltd) were merged to form the University of Technology, Mauritius. This historic step in the field of higher education was prompted mainly by the need to cater more vigorously for the increasing demand for ICT and Management professionals in a country which is seriously committed to accelerate, by all means, the realization of its ambition to become a major service provider in the field of Information Technology, Management and Finance, Sustainable Development and allied areas. The University started operation by the end of year 2000. The Board of Governors (B.O.G), was in place and in year 2001, created its senior management personnel.</p>

            <p align="justify">
  The UTM became fully operational in August 2001. Initially two Schools were created: the School of Business Informatics and Software Engineering (SOBISE) and the School of Public Sector Policy and Management (SOPSPAM) to welcome their first cohorts of undergraduate students.</p>

